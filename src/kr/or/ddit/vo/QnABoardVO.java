package kr.or.ddit.vo;

import java.io.Serializable;

public class QnABoardVO implements Serializable{
	
	private int qa_no; // 글 번호
	private String qa_title; // 글 제목
	private String qa_writer; // 글쓴이
	private String qa_content; //글내용
	private String qa_re; //답변
	private boolean qa_re_check; //답변여부
	private int mem_no; //회원번호
	public QnABoardVO() {

	}
	@Override
	public String toString() {
		return "QnABoardVO [qa_no=" + qa_no + ", qa_title=" + qa_title
				+ ", qa_writer=" + qa_writer + ", qa_content=" + qa_content
				+ ", qa_re=" + qa_re + ", qa_re_check=" + qa_re_check
				+ ", mem_no=" + mem_no + "]";
	}
	public int getQa_no() {
		return qa_no;
	}
	public void setQa_no(int qa_no) {
		this.qa_no = qa_no;
	}
	public String getQa_title() {
		return qa_title;
	}
	public void setQa_title(String qa_title) {
		this.qa_title = qa_title;
	}
	public String getQa_writer() {
		return qa_writer;
	}
	public void setQa_writer(String qa_writer) {
		this.qa_writer = qa_writer;
	}
	public String getQa_content() {
		return qa_content;
	}
	public void setQa_content(String qa_content) {
		this.qa_content = qa_content;
	}
	public String getQa_re() {
		return qa_re;
	}
	public void setQa_re(String qa_re) {
		this.qa_re = qa_re;
	}
	public boolean isQa_re_check() {
		return qa_re_check;
	}
	public void setQa_re_check(boolean qa_re_check) {
		this.qa_re_check = qa_re_check;
	}
	public int getMem_no() {
		return mem_no;
	}
	public void setMem_no(int mem_no) {
		this.mem_no = mem_no;
	}
	
	
	
	
}
